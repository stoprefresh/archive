public class BigLetters {
  public static void main(String[] args) {

    System.out.println("M       M     A     M       M");
    System.out.println("MM     MM    A A    MM     MM");
    System.out.println("M  M  M M   A   A   M  M  M M");
    System.out.println("M   M   M  A     A  M   M   M");
    System.out.println("M       M  AAAAAAA  M       M");
    System.out.println("M       M  A     A  M       M");
    System.out.println("M       M  A     A  M       M");
  }
}
