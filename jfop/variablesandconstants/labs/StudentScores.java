public class StudentScores {
  public static void main(String[] args) {
    int score1 = 75, score2 = 95, score3 = 65, score4 = 80;

    int average = (score1 + score2 + score3 + score4) / 4;

    System.out.println("Score 1: " + score1 + "\nScore 2: " + score2 + "\nScore 3: " + score3 + "\nScore 4: " + score4 + "\nAverage: " + average);
  }
}
